export async function seedSleeves(db: ReturnType<typeof drizzle>, userId?: string) {
  console.log("📂 Seeding sleeves...");

  const now = Date.now();
  
  const targetUserId = userId || "demo-user";
  console.log(`✅ Using user ID for sleeves: ${targetUserId}`);
  
  // Clear existing data for this user and any sleeves with the same IDs we're about to insert
  const { eq, inArray, or } = await import("drizzle-orm");
  
  // Get the IDs we're about to use from SLEEVES_DATA
  const sleeveIdsToInsert = SLEEVES_DATA.map(s => s.id);
  
  // Get all sleeves that either belong to this user OR have IDs we want to use
  const sleevesToDelete = await db
    .select({ id: schema.sleeve.id })
    .from(schema.sleeve)
    .where(
      or(
        eq(schema.sleeve.userId, targetUserId),
        inArray(schema.sleeve.id, sleeveIdsToInsert)
      )
    );
  
  const sleeveIdsToDelete = sleevesToDelete.map(s => s.id);
  
  if (sleeveIdsToDelete.length > 0) {
    // Delete related data first (foreign key constraints)
    await db.delete(schema.restrictedSecurity).where(inArray(schema.restrictedSecurity.sleeveId, sleeveIdsToDelete));
    await db.delete(schema.sleeveMember).where(inArray(schema.sleeveMember.sleeveId, sleeveIdsToDelete));
    await db.delete(schema.sleeve).where(inArray(schema.sleeve.id, sleeveIdsToDelete));
  }

  // Insert sleeves
  for (const sleeve of SLEEVES_DATA) {
    await db.insert(schema.sleeve).values({
      id: sleeve.id,
      userId: targetUserId, // Use target user ID
      name: sleeve.name,
      createdAt: now,
      updatedAt: now,
    });
  }

  // Insert sleeve members
  for (const member of SLEEVE_MEMBERS_DATA) {
    await db.insert(schema.sleeveMember).values({
      id: member.id,
      sleeveId: member.sleeveId,
      ticker: member.ticker,
      rank: member.rank,
      createdAt: now,
      updatedAt: now,
    });
  }

  console.log(
    `✅ Seeded ${SLEEVES_DATA.length} sleeves, ${SLEEVE_MEMBERS_DATA.length} sleeve members`
  );
}
